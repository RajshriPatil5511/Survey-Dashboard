 using Microsoft.AspNetCore.Identity;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Authorization;
using System.Net;


namespace VRS3LOGIN_AUTHENTICATION.Controllers
{
    [Authorize(Roles = "SuperAdmin")]
    public class UserRolesController : Controller
    {
        private readonly RoleManager<IdentityRole> _roleManager;

        public UserRolesController(RoleManager<IdentityRole> roleManager) 
        {
            _roleManager = roleManager;
        }

        // list All the roles created by user 

        public IActionResult Index()
        {
            var roles = _roleManager.Roles;
            return View(roles);

        }
        [HttpGet]
        public IActionResult create() { 
            
            return View();   
        
        }
        
        [HttpPost]
        public Task<IActionResult> Create(IdentityRole model)
        { // avoid dublicate code 
            if (!_roleManager.RoleExistsAsync(model.Name).GetAwaiter().GetResult())
            {

                _roleManager.CreateAsync(new IdentityRole(model.Name)).GetAwaiter().GetResult();

            }
            return Task.FromResult<IActionResult>(RedirectToAction("Index"));

        }


    }
}


















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































